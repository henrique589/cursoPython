# O sinal de + pode ser usado para fazer concatenação com strings.

concatenacao = 'Henrique' + ' ' + 'Azevedo'
print(concatenacao)

# O sinal de multiplicação pode fazer a repetição de algum elemento.
a_dez_vezes = 'A ' * 10
tres_vezes_henrique = 'Henrique' * 3
print(a_dez_vezes)
print(tres_vezes_henrique)